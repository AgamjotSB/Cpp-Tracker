#include <iostream>
using namespace std;

int main()
{
    // addition
    cout << 5 + 2 << endl; // result 7
    // subtraction
    cout << 5 - 2 << endl; // result 3
    // multipliation
    cout << 5 * 2 << endl; // result 10
    // integer division
    cout << 5 / 2 << endl; // result 2
    // double / float division
    cout << 5.0 / 2 << endl; // result 2.5
    // double / float division
    cout << 5/2.0 << endl; // result 2.5
    // modulus
    cout << 5%2 << endl; //result 1


    // increment
    int total = 1;
    cout << total++ << endl; // result 2
    // decrement variable--
    //specific value done by += or -=

}